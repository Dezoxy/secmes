#!/usr/bin/env node
/* global process */
// Generate the About-page changelog from git tags + conventional commits → src/generated/release-notes.generated.ts.
//
// Runs OUTSIDE Docker (the build context excludes .git), so CD invokes this in the runner job BEFORE the image
// build (with fetch-depth: 0); the emitted .ts then rides into the build context and is baked into the bundle.
// A committed copy is the fallback so typecheck/test/dev resolve without running git. Plain node (no deps).
//
//   node apps/web/scripts/gen-release-notes.mjs        # from anywhere; resolves paths off this file
//
// Release versioning: tags `v*` (prod) and `aws-v*` (experiment), newest-first; each release lists the
// feat/fix commits since the previous tag. No tags yet → a single "Development build" entry from recent HEAD.

import { execFileSync } from 'node:child_process';
import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

import {
  buildReleaseNotes,
  normalizeTagVersion,
  buildReleaseEntry,
} from './release-notes-core.mjs';

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(here, '../../..');
const outFile = resolve(here, '../src/generated/release-notes.generated.ts');

function git(args) {
  return execFileSync('git', args, { cwd: repoRoot, encoding: 'utf8' });
}

/** Subjects (newest-first) in `range`, merges excluded. `range` is e.g. "v0.3.0..v0.4.0" or a single ref. */
function subjectsIn(range) {
  const out = git(['log', '--no-merges', '--format=%s', ...range]).trim();
  return out ? out.split('\n') : [];
}

function releaseTagsNewestFirst() {
  let out;
  try {
    out = git([
      'for-each-ref',
      '--sort=-creatordate',
      '--format=%(refname:short)\t%(creatordate:short)',
      'refs/tags/v*',
      'refs/tags/aws-v*',
    ]).trim();
  } catch {
    return []; // not a git repo / no tags reachable → fall back to the dev entry
  }
  if (!out) return [];
  return out.split('\n').map((line) => {
    const [tag, date] = line.split('\t');
    return { tag, date };
  });
}

function buildNotes() {
  const tags = releaseTagsNewestFirst();

  if (tags.length === 0) {
    // No releases tagged yet — a single dev entry from recent HEAD so About isn't blank in dev/test.
    const entry = buildReleaseEntry({
      version: 'dev',
      date: 'Development build', // buildReleaseEntry uses `date` as the entry title
      subjects: subjectsIn(['-n', '30', 'HEAD']),
    });
    return entry ? [entry] : [];
  }

  const perTag = tags.map(({ tag, date }, i) => {
    const prev = tags[i + 1]?.tag; // next-older tag, or undefined for the oldest (→ full history up to it)
    const range = prev ? [`${prev}..${tag}`] : [tag];
    return { version: `v${normalizeTagVersion(tag)}`, date, subjects: subjectsIn(range) };
  });
  return buildReleaseNotes(perTag);
}

const notes = buildNotes();
const banner =
  '// AUTO-GENERATED by apps/web/scripts/gen-release-notes.mjs — DO NOT EDIT.\n' +
  '// Regenerated in CD from git tags + commits before the image build; committed copy is the dev/test fallback.\n';
const body =
  banner +
  "import type { ReleaseNote } from '../lib/release-notes-types';\n\n" +
  `export const releaseNotes: ReleaseNote[] = ${JSON.stringify(notes, null, 2)};\n`;

mkdirSync(dirname(outFile), { recursive: true });
writeFileSync(outFile, body);
process.stderr.write(`gen-release-notes: wrote ${notes.length} release(s) → ${outFile}\n`);
